    private void fillTable(List<SanPhamChiTiet> l) {
        dtm = (DefaultTableModel) tblspct.getModel();
        dtm.setRowCount(0);
        for (SanPhamChiTiet x : l) {
            dtm.addRow(new Object[]{
                x.getId(), lblmasp.getText(), lbltensp.getText(), pls.findPL(x.getIdpl()).getTen(), cls.findCL(x.getIdcl()).getTen(), ths.findTH(x.getIdth()).getTen(), mss.findMS(x.getIdms()).getTen(),
                xxs.findXX(x.getIdxx()).getTen(), x.getKichThuoc(), x.getKhoiLuong(), x.getSoLuong(), x.getGia(), x.getMoTa(), x.getAnh()
            });
        }
    }

    private void LoadCBO() {
        cbochatlieu.removeAllItems();
        cbomausac.removeAllItems();
        cbophanloai.removeAllItems();
        cbothuonghieu.removeAllItems();
        cboxuatxu.removeAllItems();
        cbochatlieu1.removeAllItems();
        cbomausac1.removeAllItems();
        cbophanloai1.removeAllItems();
        cbothuonghieu1.removeAllItems();
        cboxuatxu1.removeAllItems();

        cbochatlieu1.addItem("Tất cả");
        cbomausac1.addItem("Tất cả");
        cbophanloai1.addItem("Tất cả");
        cbothuonghieu1.addItem("Tất cả");
        cboxuatxu1.addItem("Tất cả");
        for (ChatLieu x : cls.gettAll()) {
            cbochatlieu.addItem(x.getTen());
            cbochatlieu1.addItem(x.getTen());
        }
        for (PhanLoai x : pls.gettAll()) {
            cbophanloai.addItem(x.getTen());
            cbophanloai1.addItem(x.getTen());
        }
        for (MauSac x : mss.gettAll()) {
            cbomausac.addItem(x.getTen());
            cbomausac1.addItem(x.getTen());
        }
        for (ThuongHieu x : ths.gettAll()) {
            cbothuonghieu.addItem(x.getTen());
            cbothuonghieu1.addItem(x.getTen());
        }
        for (XuatXu x : xxs.gettAll()) {
            cboxuatxu.addItem(x.getTen());
            cboxuatxu1.addItem(x.getTen());
        }
    }

