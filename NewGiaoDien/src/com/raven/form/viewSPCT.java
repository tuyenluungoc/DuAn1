    public boolean Check() {
        Boolean c = true;

        try {
            Integer.parseInt(txtsoluong.getText());
            c = true;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số nguyên");
            c = false;
        }
        try {
            Double.parseDouble(txtkhoiluong.getText());
            c = true;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Khối lượng phải là số");
            c = false;
        }
        try {
            Double.parseDouble(txtgia.getText());
            c = true;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Giá tiền phải là số");
            c = false;
        }
        if (txtkhoiluong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Khối lượng không trống");
            c = false;
        } else {
            if (Float.parseFloat(txtkhoiluong.getText()) <= 0) {
                JOptionPane.showMessageDialog(this, "Khối lượng phải lớn hơn 0");
                c = false;
            }
        }
        if (txtkichthuoc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kích thước không trống");
            c = false;
        }
        if (txtsoluong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số lượng không trống");
            c = false;
        } else {
            if (Integer.parseInt(txtsoluong.getText()) <= 0) {
                JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0");
                c = false;
            }
        }
        if (txtgia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Giá tiền không trống");
            c = false;
        } else {
            if (Integer.parseInt(txtsoluong.getText()) <= 0) {
                JOptionPane.showMessageDialog(this, "Giá tiền phải lớn hơn 0");
                c = false;
            }
        }
        for (SanPhamChiTiet x : service.gettAll()) {
            if (x.getGia().equals(readFormSPCT().getGia()) && x.getIdcl().equals(readFormSPCT().getIdcl()) && x.getIdms().equals(readFormSPCT().getIdms())
                    && x.getIdpl().equals(readFormSPCT().getIdpl()) && x.getIdth().equals(readFormSPCT().getIdth()) && x.getIdxx().equals(readFormSPCT().getIdxx()) && x.getKhoiLuong().equals(readFormSPCT().getKhoiLuong())
                    && x.getKichThuoc().equals(readFormSPCT().getKichThuoc())) {
                JOptionPane.showMessageDialog(this, "Đã tồn tại sản phẩm như vậy không thể thêm");
                c = false;
            }
        }
        return c;
    }

